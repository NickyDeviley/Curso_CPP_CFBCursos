#include <iostream>

using namespace std;

int main(int argc, char *argv[]) {

    /*
        O laço FOR é outra estrutura de repetição de código,
        mas ao contrário do Loop While, o FOR executa o bloco
        de código é executado um número específico de vezes.
    */

    for (int i = 0; i < 10; i++) {

        cout << i << endl;  // 0 -> 1 -> 2 -> ... -> 9

    }

    return 0;
}